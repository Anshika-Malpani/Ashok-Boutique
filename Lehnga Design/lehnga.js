function displayImage(src) {
    var modal = document.getElementById('imageModal');
    var img = document.getElementById('expandedImage');
    img.src = src;
    modal.style.display = 'block';
}

// Function to close the modal
function closeModal() {
    var modal = document.getElementById('imageModal');
    modal.style.display = 'none';
}

